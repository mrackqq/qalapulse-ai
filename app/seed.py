USERS = [None, "Житель района", "Айдана", "Ержан", "Аноним", "Дана", "Нурлан", "Сауле"]


def _coords(place: str, rng: random.Random, tight: bool = False) -> tuple[str, str, float, float]:
    district, address, lat, lon = PLACES[place]
    # Jitter удерживаем у реального якоря: обычные точки <= ~25 м, кластеры дублей <= ~10 м.
    spread = 0.00007 if tight else 0.00018
    lat += rng.uniform(-spread, spread)
    lon += rng.uniform(-spread * 1.3, spread * 1.3)
    return district, address, round(lat, 6), round(lon, 6)


def _create(db, rng, entry, tight=False):
    text, category, place, status, age_hours = entry
    _district, address, lat, lon = _coords(place, rng, tight=tight)
    created_at = datetime.utcnow() - timedelta(hours=age_hours, minutes=rng.randint(0, 59))
    issue, _ = create_issue(
        db=db,
        user_name=rng.choice(USERS),
        text=text,
        address_text=f"{address}, Астана",
        latitude=lat,
        longitude=lon,
        category_hint=category,
        source="demo",
        created_at=created_at,
    )
    if status != "new":
        set_issue_status(db, issue.id, status, _status_comment(status))
    return issue


def _status_comment(status: str) -> str:
    return {
        "in_progress": "Служба приняла заявку в работу",
        "resolved": "Проблема устранена, восстановление подтверждено",
        "rejected": "Закрыто: вне зоны ответственности или дубликат",
    }.get(status, "Demo status")


def main() -> None:
    rng = random.Random(42)
    Base.metadata.create_all(bind=engine)
    ensure_schema()
    with SessionLocal() as db:
        db.execute(delete(DuplicateLink))
        db.execute(delete(StatusHistory))
        db.execute(delete(Issue))
        db.commit()

        count = 0
        for entry in ISSUES:
            _create(db, rng, entry)
            count += 1

        for cluster in DUPLICATE_CLUSTERS:
            for entry in cluster:
                _create(db, rng, entry, tight=True)
                count += 1

        _rebalance_overdue_new(db, rng, keep=3)

    print(f"Seeded {count} curated demo issues for QalaPulse AI.")


def _rebalance_overdue_new(db, rng, keep: int = 3) -> None:
    """Realistic SLA UX: свежие 'new' должны быть в срок.
    Оставляем 2-4 старых просроченных 'new' для демонстрации SLA, а остальные
    старые открытые заявки переводим в in_progress / resolved / rejected.
    """
    issues = db.scalars(select(Issue)).all()
    for issue in issues:
        refresh_issue_sla(issue)
    overdue_new = sorted(
        (i for i in issues if i.status == "new" and i.sla_status == "overdue"),
        key=lambda i: i.priority_score,
        reverse=True,
    )
    stages = ["in_progress", "resolved", "in_progress", "rejected", "resolved", "in_progress"]
    for index, issue in enumerate(overdue_new[keep:]):
        new_status = stages[index % len(stages)]
        set_issue_status(db, issue.id, new_status, _status_comment(new_status))


if __name__ == "__main__":
    main()
